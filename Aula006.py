# Conversão de tipos, coerção 
# Type convertion, typecasting, coercion
# É o ato de converter um tipo em outo
# Tipos imutáveis e primitivos:
# str, int, float, bool

print(1 + 1)
print('a' + 'b')

# Converte para inteiro
print(float('9.5') + 0.5)
print(type(float('9.5') + 0.5))

print(int('9') + 1)
print(type(int('9') + 1))

print('a' + str(1))
print(type('a' + str(1)))